# Fletライブラリをftという名前でインポートする。
# これがFletアプリケーションを構築するための基本となる。
import flet as ft

def main(page: ft.Page):
    """
    Fletアプリケーションのメイン関数。
    この関数がアプリケーションのUIとロジックを定義する。
    `page`引数は、アプリケーションのウィンドウやページ全体を表すオブジェクトである。
    """
    
    # --- アプリケーションウィンドウの基本設定 ---
    # ウィンドウの上部に表示されるタイトルを設定する。
    page.title = "天気予報アプリ"
    # アプリケーションの見た目をライトモードに設定する。
    page.theme_mode = ft.ThemeMode.LIGHT
    
    # --- 画面に表示するコントロール（ウィジェット）を追加 ---
    # `page.add()`を使って、画面に表示したいコントロールを追加していく。
    # ここでは、単純なテキストコントロールを追加している。
    page.add(
        ft.Text("ようこそ！")
    )
    
    # 最後に画面を更新して、変更を反映させる。
    page.update()

if __name__ == "__main__":
    ft.app(target=main)